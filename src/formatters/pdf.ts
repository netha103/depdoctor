function renderFixSection(pdf: PagedPDF, fixResult: FixResult): void {
  pdf.ensureSpace(50);
  pdf.y = drawSectionHeader(
    pdf.doc,
    pdf.y,
    'Fix Results',
    fixResult.applied.length + fixResult.skipped.length,
  );

  // Applied fixes
  if (fixResult.applied.length > 0) {
    pdf.ensureSpace(20);
    pdf.doc.save()
       .fillColor(COLORS.success)
       .fontSize(9).font('Helvetica-Bold')
       .text(`✓ Applied (${fixResult.applied.length})`, MARGIN + 4, pdf.y, { lineBreak: false })
       .restore();
    pdf.y += 16;

    const cols: ColumnDef[] = [
      { header: 'Type',        width: 110 },
      { header: 'File',        width: 180 },
      { header: 'Description', width: CONTENT_WIDTH - 290 },
    ];
    pdf.ensureSpace(16);
    pdf.y = drawTableHeader(pdf.doc, pdf.y, cols);

    for (let i = 0; i < fixResult.applied.length; i++) {
      pdf.ensureSpace(14);
      const action = fixResult.applied[i];
      if (!action) continue;
      pdf.y = drawTableRow(pdf.doc, pdf.y, cols, [
        action.type,
        action.file ?? '—',
        action.description,
      ], i);
    }
    pdf.y += 10;
  }

  // Skipped fixes
  if (fixResult.skipped.length > 0) {
    pdf.ensureSpace(20);
    pdf.doc.save()
       .fillColor(COLORS.muted)
       .fontSize(9).font('Helvetica-Bold')
       .text(`⤷ Skipped (${fixResult.skipped.length})`, MARGIN + 4, pdf.y, { lineBreak: false })
       .restore();
    pdf.y += 16;

    const cols: ColumnDef[] = [
      { header: 'Type',        width: 110 },
      { header: 'File',        width: 180 },
      { header: 'Description', width: CONTENT_WIDTH - 290 },
    ];
    pdf.ensureSpace(16);
    pdf.y = drawTableHeader(pdf.doc, pdf.y, cols);

    for (let i = 0; i < fixResult.skipped.length; i++) {
      pdf.ensureSpace(14);
      const action = fixResult.skipped[i];
      if (!action) continue;
      pdf.y = drawTableRow(pdf.doc, pdf.y, cols, [
        action.type,
        action.file ?? '—',
        action.description,
      ], i);
    }
    pdf.y += 10;
  }

  // Errors
  if (fixResult.errors.length > 0) {
    pdf.ensureSpace(20);
    pdf.doc.save()
       .fillColor(COLORS.danger)
       .fontSize(9).font('Helvetica-Bold')
       .text(`✗ Errors (${fixResult.errors.length})`, MARGIN + 4, pdf.y, { lineBreak: false })
       .restore();
    pdf.y += 14;

    for (const errMsg of fixResult.errors) {
      pdf.ensureSpace(12);
      pdf.doc.save()
         .fillColor(COLORS.danger)
         .fontSize(7.5).font('Helvetica')
         .text(`• ${errMsg}`, MARGIN + 8, pdf.y, { width: CONTENT_WIDTH - 16, lineBreak: false })
         .restore();
      pdf.y += 12;
    }
  }

  if (fixResult.backupPath !== null) {
    pdf.ensureSpace(14);
    pdf.doc.save()
       .fillColor(COLORS.muted)
       .fontSize(7.5).font('Helvetica')
       .text(`Backup: ${fixResult.backupPath}`, MARGIN + 4, pdf.y, { lineBreak: false })
       .restore();
    pdf.y += 14;
  }
}

// ─── Cover / metadata block ───────────────────────────────────────────────────

function renderMeta(pdf: PagedPDF, result: ScanResult): void {
  const totalIssues =
    result.dependencyIssues.length +
    result.importIssues.length +
    result.variableIssues.length +
    result.securityIssues.length +
    result.envIssues.length;

  // Status line
  const statusBg = totalIssues === 0 ? COLORS.success : COLORS.warning;
  const statusText =
    totalIssues === 0
      ? 'No issues found'
      : `${totalIssues} issue(s) found`;

  pdf.doc.save()
     .roundedRect(MARGIN, pdf.y, CONTENT_WIDTH, 26, 5)
     .fill(statusBg)
     .fillColor(COLORS.white)
     .fontSize(12).font('Helvetica-Bold')
     .text(statusText, MARGIN, pdf.y + 7, { width: CONTENT_WIDTH, align: 'center', lineBreak: false })
     .restore();
  pdf.y += 34;

  // Meta row
  pdf.doc.save()
     .fillColor(COLORS.muted)
     .fontSize(8).font('Helvetica')
     .text(`Project: ${result.projectRoot}`, MARGIN, pdf.y)
     .text(`Scanned: ${result.filesScanned} file(s) in ${result.durationMs}ms`, MARGIN, pdf.y + 12)
     .text(`Date: ${new Date().toUTCString()}`, MARGIN, pdf.y + 24)
     .restore();
  pdf.y += 42;

  // Summary cards
  const cards: CardData[] = [
    { label: 'Unused Deps',   value: result.dependencyIssues.length, color: COLORS.accent   },
    { label: 'Unused Imports',value: result.importIssues.length,     color: COLORS.primary  },
    { label: 'Unused Vars',   value: result.variableIssues.length,   color: COLORS.darkGray },
    { label: 'Security',      value: result.securityIssues.length,   color: COLORS.danger   },
    { label: 'Env Issues',    value: result.envIssues.length,        color: COLORS.warning  },
  ];
  pdf.y = drawSummaryCards(pdf.doc, pdf.y, cards);

  drawHRule(pdf.doc, pdf.y);
  pdf.y += 12;
}

// ─── Public API ───────────────────────────────────────────────────────────────

/**
 * Generate a PDF scan report and write it to `outputPath`.
 * Returns a Promise that resolves when the file is fully written.
 */
export function generatePdfReport(
  result: ScanResult,
  outputPath: string,
  fixResult?: FixResult,
): Promise<void> {
  return new Promise((resolve, reject) => {
    const title = fixResult !== undefined ? 'Fix Report' : 'Scan Report';
    const pdf = new PagedPDF(title);
    const stream = fs.createWriteStream(outputPath);

    stream.on('error', reject);
    stream.on('finish', resolve);

    pdf.pipe(stream);

    renderMeta(pdf, result);
    renderDependencySection(pdf, result.dependencyIssues);
    renderImportSection(pdf, result.importIssues);
    renderVariableSection(pdf, result.variableIssues);
    renderSecuritySection(pdf, result.securityIssues);
    renderEnvSection(pdf, result.envIssues);

    if (fixResult !== undefined) {
      renderFixSection(pdf, fixResult);
    }

    pdf.finalize();
    pdf.end();
  });
}
