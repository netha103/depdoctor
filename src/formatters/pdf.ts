function renderSecuritySection(pdf: PagedPDF, issues: SecurityIssue[]): void {
  if (issues.length === 0) return;
  pdf.ensureSpace(50);
  pdf.y = drawSectionHeader(pdf.doc, pdf.y, 'Security Issues', issues.length);

  const order: SecuritySeverity[] = ['critical', 'high', 'moderate', 'low', 'info'];
  const grouped = new Map<SecuritySeverity, SecurityIssue[]>();
  for (const sev of order) grouped.set(sev, []);
  for (const issue of issues) grouped.get(issue.severity)?.push(issue);

  for (const sev of order) {
    const group = grouped.get(sev) ?? [];
    if (group.length === 0) continue;

    pdf.ensureSpace(20);
    const color = SEVERITY_COLOR[sev];
    pdf.doc.save()
       .rect(MARGIN, pdf.y, CONTENT_WIDTH, 16)
       .fill(color)
       .fillColor(COLORS.white)
       .fontSize(8).font('Helvetica-Bold')
       .text(`${secSeverityLabel(sev)} (${group.length})`, MARGIN + 8, pdf.y + 4, { lineBreak: false })
       .restore();
    pdf.y += 16;

    const cols: ColumnDef[] = [
      { header: 'Package',        width: 130 },
      { header: 'Type',           width: 90  },
      { header: 'Description',    width: 220 },
      { header: 'Recommendation', width: CONTENT_WIDTH - 440 },
    ];

    pdf.ensureSpace(16);
    pdf.y = drawTableHeader(pdf.doc, pdf.y, cols);

    for (let i = 0; i < group.length; i++) {
      pdf.ensureSpace(14);
      const issue = group[i];
      if (!issue) continue;
      pdf.y = drawTableRow(pdf.doc, pdf.y, cols, [
        issue.package,
        issue.type,
        issue.description,
        issue.recommendation ?? '—',
      ], i);
    }

    pdf.y += 8;
  }

  pdf.y += 4;
}

